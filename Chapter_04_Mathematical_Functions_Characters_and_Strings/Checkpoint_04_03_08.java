public class Checkpoint_04_03_08 {
    public static void main(String[] args) {
        System.out.println("'a' < 'b' is " + ('a' < 'b'));
        System.out.println("'a' <= 'A' is " + ('a' <= 'A'));
        System.out.println("'a' > 'b' is " + ('a' > 'b'));
        System.out.println("'a' >= 'A' is " + ('a' >= 'A'));
        System.out.println("'a' == 'a' is " + ('a' == 'a'));
        System.out.println("'a' != 'b' is " + ('a' != 'b'));
    }
}
