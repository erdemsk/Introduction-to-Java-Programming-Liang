public class Checkpoint_04_03_01 {
    public static void main(String[] args) {

        System.out.println("ASCII code of '1' is: " + (byte) '1');
        System.out.println("ASCII code of 'A' is: " + (byte) 'A');
        System.out.println("ASCII code of 'B' is: " + (byte) 'B');
        System.out.println("ASCII code of 'a' is: " + (byte) 'a');
        System.out.println("ASCII code of 'b' is: " + (byte) 'b');
        System.out.println();

        System.out.println("Character for the decimal code 40 is: " + (char) 40);
        System.out.println("ACharacter for the decimal code 59 is: " + (char) 59);
        System.out.println("Character for the decimal code 79 is: " + (char) 79);
        System.out.println("Character for the decimal code 85 is: " + (char) 85);
        System.out.println("Character for the decimal code 90 is: " + (char) 90);
        System.out.println();

        System.out.println("Character for the hexadecimal code 40 is: "+ (char) 0x40);
        System.out.println("Character for the hexadecimal code 5A is: "+ (char) 0x5A);
        System.out.println("Character for the hexadecimal code 71 is: "+ (char) 0x71);
        System.out.println("Character for the hexadecimal code 72 is: "+ (char) 0x72);
        System.out.println("Character for the hexadecimal code 7A is: "+ (char) 0x7A);


    }
}
